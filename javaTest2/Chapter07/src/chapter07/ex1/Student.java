package chapter07.ex1;

public class Student {
	private int number;
	public String name;
	
	public Student() {};
	
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
